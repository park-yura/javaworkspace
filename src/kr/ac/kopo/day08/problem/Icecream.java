public class Icecream {
	
	String name; //아이스크림 이름 
	int price; //아이스크림 가격
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getPrice() {
		return price;
	}
	
	public void setPrice() {
		this.price = price;
	}
}
